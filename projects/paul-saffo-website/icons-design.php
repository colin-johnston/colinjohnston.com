<h3>Icon Design</h3>
<p>Sketches and vector artwork for new site navigation icons.</p>
<h5>Sketches</h5>

<p>The new icons were based closely on very small 16-pixel wide GIF files used in Paul's older website. I set out to
    re-design each icon to be optically balanced at a wide range of sizes.</p>
